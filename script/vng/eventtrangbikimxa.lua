		Talk(1,"",szNpcName.."RÊt tiÕc, ng­¬i vÉn cßn thua nhiÒu ng­êi, hiÖn ch­a lät vµo danh s¸ch 100 ng­êi dÉn ®Çu.")
		return 0	
	end
	
