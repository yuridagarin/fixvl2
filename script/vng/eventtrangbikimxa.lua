		szResult = "\nHiÖn t¹i ng­¬i ®ang ®øng h¹ng <color=red>"..nRank.."<color> trong danh s¸ch, víi sè ®iÓm lµ <color=yellow>"..LIB_txtData.tbTextData[nRank][1].."<color>\n"
